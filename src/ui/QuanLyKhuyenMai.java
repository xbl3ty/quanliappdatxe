    void selectImage() {
        JFileChooser filee = new JFileChooser();
        if (filee.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = filee.getSelectedFile();
            if (saveLogo(file)) {
                lblHinh.setText("");
                // Hiển thị hình lên form
                lblHinh.setIcon(readLogo(file.getName()));
                lblHinh.setToolTipText(file.getName());
            }
        }
    }

    public List getModel() {
        List model = new ArrayList();
        model.add(txtmakm.getText());
        model.add(txtten.getText());
        model.add(txtLoai.getText());
        model.add(txtSL.getText());
        String date = sf.format(jDateChooser1.getDate());
        date += gio.getText() + ":" + phut.getText() + ":00";
        String date2 = sf.format(jDateChooser2.getDate());
        date2 += gio.getText() + ":" + phut.getText() + ":00";
        model.add(date);
        model.add(date2);
        model.add(txtpt.getText());
        model.add(lblHinh.getToolTipText());
        return model;
    }

