    DefaultTableModel df = (DefaultTableModel) table.getModel();
             if(row<=0){
                 JOptionPane.showMessageDialog(this, "Đã ở đầu");
                 row=0;
             }else{
                 row=row-1;
             }
             
            txtmakm.setText(df.getValueAt(row, 0).toString());
